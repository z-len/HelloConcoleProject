// Задача. Поставим три точки, найдем середину отрезка между двумая вершинами и поставим точку...
Console.Clear(); // сначала очищаем консоль
//Console.SetCursorPosition(10, 4);// специальная команда рисования. В скобках обозначает, что мы отступаем от левого края 10 позиций и от верхнего края 4 позиции
//Console.WriteLine("+");// точки будем ставить плюсами
//int xa = 1; // координата первой точки по иску, то есть отступ слева
//int ya = 1;// координата первой точки по игреку, то есть отступ сверху
